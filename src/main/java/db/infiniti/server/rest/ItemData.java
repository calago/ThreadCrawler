package db.infiniti.server.rest;

/**
 * A POJO for crawler itemdata
 * @author root
 *
 */
public class ItemData
{

    String label;
    String xpath;
    
    public String getLabel()
    {
        return label;
    }
    public void setLabel(String label)
    {
        this.label = label;
    }
    public String getXpath()
    {
        return xpath;
    }
    public void setXpath(String xpath)
    {
        this.xpath = xpath;
    }

    
}
